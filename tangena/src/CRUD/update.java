    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            url = "jdbc:mysql://localhost:3306/portal";
            connection = DriverManager.getConnection(url, "root","");
            st = connection.createStatement();
            if(idField.getText().equals("") || passwordField.getText().equals("") || surnameField.getText().equals("") || firstnameField.getText().equals("")){
                JOptionPane.showMessageDialog(null, "ENTER INFO TO ALL FIELDS","System Message",JOptionPane.ERROR_MESSAGE);
            }
            else{
                rs = st.executeQuery("SELECT id,password FROM students");
                String tempID = "", tempPass = "";
                while(rs.next()){
                    String dataId = rs.getString("id");
                    String dataPass = rs.getString("password");

                    if(dataId.equals(idField.getText()) && dataPass.equals(passwordField.getText())){
                        st.executeQuery("SELECT * FROM students WHERE id='" + idField.getText() + "' and password='" + passwordField.getText()+"'");
                        st.executeUpdate("UPDATE students SET  surname= '"+surnameField.getText()+"',firstname= '"+firstnameField.getText()+"', birthday= '"+birthdayField.getText()+"',gender= '"+(String)genderComb.getSelectedItem()+"'Where id = '"+idField.getText()+"' ");
                        JOptionPane.showMessageDialog(null, "ACCOUNT UPDATED","System Message",JOptionPane.INFORMATION_MESSAGE);
                        tempID = idField.getText();
                        tempPass = passwordField.getText();
                        idField.setText("");
                        passwordField.setText("");
                        surnameField.setText("");
                        firstnameField.setText("");
                        birthdayField.setText("MM/DD/YY");
                        genderComb.setSelectedIndex(0);
                        break;
                    } 
                }
                if(tempID.equals("") || tempPass.equals("")){
                    JOptionPane.showMessageDialog(null, "INVALID ID OR PASSWORD","System Message",JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (ClassNotFoundException ex) {
            //Logger.getLogger(create.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "INVALID ACCOUNT!","System Message",JOptionPane.ERROR_MESSAGE);
            idField.setText("");
            passwordField.setText("");
            
        }
    }//GEN-LAST:event_jButton2ActionPerformed

